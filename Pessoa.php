<?php

class Pessoa implements EntityInterface
{

    private $id;
    private $nome;
    private $sexo;
    private $cpf;
    private $rg;

    /**
     * Pessoa constructor.
     * @param $data
     */
    function __construct($data)
    {
        $this->hidrator($data);
    }

    /**
     * Transforma o array de dados em um novo objeto
     *
     * @param $data
     */
    public function hidrator($data)
    {
        foreach ($data as $key => $value) {
            $nomeFuncao = "set" . ucfirst($key);
            if (method_exists($this, $nomeFuncao) and null !== $value) {
                $this->{$nomeFuncao}($value);
            }
        }
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getNome()
    {
        return $this->nome;
    }

    /**
     * @param mixed $nome
     */
    public function setNome($nome)
    {
        $this->nome = $nome;
    }

    /**
     * @return mixed
     */
    public function getIdade()
    {
        return $this->idade;
    }

    /**
     * @param mixed $idade
     */
    public function setIdade($idade)
    {
        $this->idade = $idade;
    }

    /**
     * @return mixed
     */
    public function getSexo()
    {
        return $this->sexo;
    }

    /**
     * @param mixed $sexo
     */
    public function setSexo($sexo)
    {
        $this->sexo = $sexo;
    }

    /**
     * @return mixed
     */
    public function getCpf()
    {
        return $this->cpf;
    }

    /**
     * @param mixed $cpf
     */
    public function setCpf($cpf)
    {
        $this->cpf = $cpf;
    }

    /**
     * @return mixed
     */
    public function getRg()
    {
        return $this->rg;
    }

    /**
     * @param mixed $rg
     */
    public function setRg($rg)
    {
        $this->rg = $rg;
    }
}